    private void jbtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSaveActionPerformed
        String firstName = jtxtFirstName.getText();
        String lastName = jtxtLastName.getText();
        String username = jtxtUsername.getText();
        String pasword = jtxtPassword.getText();
        MovieTheater movieTheater = (MovieTheater) jcmbboxAllMovieTheaters.getSelectedItem();
        Employee employee = Controller.getInstance().saveEmployee(new Employee(movieTheater, null, firstName, lastName, username, pasword));
        if (employee != null) {
            Controller.getInstance().getMap().put("current_employee", employee);//ako zatreba
            currentFormMode = FormMode.FORM_VIEW;
            prepareView(FormMode.FORM_VIEW);
            fillEmployee(employee);
            JOptionPane.showMessageDialog(null, "Employee " + employee.getUsername() + " saved!");
        }
    }//GEN-LAST:event_jbtnSaveActionPerformed

