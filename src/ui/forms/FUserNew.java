    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        String firstName;
        String lastName;
        String username;
        String pasword;
        if (!jtxtFirstName.getText().isEmpty() && !jtxtLastName.getText().isEmpty() && !jtxtPassword.getText().isEmpty() && !jtxtUsername.getText().isEmpty()) {
            firstName = jtxtFirstName.getText();
            lastName = jtxtLastName.getText();
            username = jtxtUsername.getText();
            pasword = jtxtPassword.getText();
            MovieTheater movieTheater = (MovieTheater) jcmbboxAllMovieTheaters.getSelectedItem();
            Employee employee = (Employee) Controller.getInstance().getMap().get("current_employee");
            employee.setFirstName(firstName);
            employee.setLastName(lastName);
            employee.setPassword(pasword);
            employee.setUsername(username);
            employee.setMovieTheater(movieTheater);
            employee = Controller.getInstance().updateEmployee(employee);

            Controller.getInstance().getMap().put("current_employee", employee);
            JOptionPane.showMessageDialog(null, "Employee " + employee.getUsername() + " updated!");
            currentFormMode = FormMode.FORM_VIEW;
            prepareView(FormMode.FORM_VIEW);
        }
    }//GEN-LAST:event_jbtnUpdateActionPerformed

