    private void prepareView(FormMode formmode) {

        List<MovieTheater> allMovieTheaters = Controller.getInstance().getAllMovieTheaters();
        for (MovieTheater movieTheater : allMovieTheaters) {
            jcmbboxAllMovieTheaters.addItem(movieTheater);

            if (formmode.equals(FormMode.FORM_ADD)) {

                jtxtLastName.setEditable(true);
                jtxtPassword.setEditable(true);
                jtxtUsername.setEditable(true);
                jtxtFirstName.setEditable(true);
                jbtnSave.setVisible(true);
                jbtnChange.setVisible(false);
                jbtnAddAnother.setVisible(false);
                jbtnUpdate.setVisible(false);

            }

            if (formmode.equals(FormMode.FORM_VIEW)) {
                jtxtLastName.setEditable(false);
                jtxtPassword.setEditable(false);
                jtxtUsername.setEditable(false);
                jtxtFirstName.setEditable(false);
                jbtnSave.setVisible(false);
                jbtnChange.setVisible(true);
                jbtnAddAnother.setVisible(true);
                jbtnUpdate.setVisible(false);
            }

            if (formmode.equals(FormMode.FORM_UPDATE)) {
                jtxtLastName.setEditable(true);
                jtxtPassword.setEditable(true);
                jtxtUsername.setEditable(true);
                jtxtFirstName.setEditable(true);
                jbtnSave.setVisible(true);
                jbtnChange.setVisible(false);
                jbtnAddAnother.setVisible(false);
                jbtnSave.setVisible(false);
                jbtnUpdate.setVisible(true);
            }
        }

    }

    private void fillEmployee(Employee employee) {
        jcmbboxAllMovieTheaters.setSelectedItem(employee.getMovieTheater());
        jtxtFirstName.setText(employee.getFirstName());
        jtxtLastName.setText(employee.getLastName());
        jtxtPassword.setText(employee.getPassword());
        jtxtUsername.setText(employee.getUsername());
    }
